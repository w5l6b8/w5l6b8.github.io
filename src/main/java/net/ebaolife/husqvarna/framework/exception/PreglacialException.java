package net.ebaolife.husqvarna.framework.exception;


public class PreglacialException extends JavaException {
	private static final long serialVersionUID = 1L;
	
	
	public PreglacialException() {
		super();
	}
	
	public PreglacialException(String message) {
		super(message);
	}
	
	public PreglacialException(Throwable cause) {
		super(cause);
	}
	
	public PreglacialException(String message, Throwable cause) {
		super(message,cause);
	}
	
	

}
