package net.ebaolife.husqvarna.framework.exception;


public class ZipException extends ProjectException {
	private static final long serialVersionUID = 1L;
	
	
	public ZipException() {
		super();
	}
	
	public ZipException(String message) {
		super(message);
	}
	
	public ZipException(Throwable cause) {
		super(cause);
	}
	
	public ZipException(String message, Throwable cause) {
		super(message, cause);
	}
	
	

	

}
